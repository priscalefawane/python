locations = ['mauritius', 'paris', 'zanzibar', 'disneyland', 'china']
print(locations)
print("Sorted list:")
print(sorted(locations))
print("Here is the original list:")
print(locations)

print("\nHere is the sorted  reversed list:")
print(sorted(locations))
print(locations)

locations.reverse()
print(locations)

locations.reverse()
print(locations)

locations.sort()
